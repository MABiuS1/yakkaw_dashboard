package services

import "testing"

func TestDeriveProvinceSampleAddresses(t *testing.T) {
	cases := map[string]string{
		"ต.วาวี อ.แม่สรวย จ.เชียงราย":                         "เชียงราย",
		" ต.ตะคร้อ อ.ไพศาลี จ.นครสวรรค์":                        "นครสวรรค์",
		" ต.สายลำโพง อ.ท่าตะโก จ.นครสวรรค์":                       "นครสวรรค์",
		" ต.ธารทหาร อ.หนองบัว จ.นครสวรรค์":                       "นครสวรรค์",
		" ต.อุดมธัญญา อ.ตากฟ้า จ.นครสวรรค์":                       "นครสวรรค์",
		" ต.จันเสน อ.ตาคลี จ.นครสวรรค์":                          "นครสวรรค์",
		"แขวงป้อมปราบ เขตป้อมปราบศัตรูพ่าย กรุงเทพมหานคร ":       "กรุงเทพมหานคร",
		"แขวงวัดเทพศิรินทร์ เขตป้อมปราบศัตรูพ่าย กรุงเทพมหานคร ": "กรุงเทพมหานคร",
		"แขวงบ้านบาตร เขตป้อมปราบศัตรูพ่าย กรุงเทพมหานคร ":       "กรุงเทพมหานคร",
		"Somewhere, Bangkok City":                                    "กรุงเทพมหานคร",
	}

	for input, expected := range cases {
		if got := deriveProvince(input); got != expected {
			t.Fatalf("deriveProvince(%q) = %q, want %q", input, got, expected)
		}
	}
}

